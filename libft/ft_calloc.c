#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*p;

	if ((count == SIZE_MAX && size > 1)
		|| (size == SIZE_MAX && count > 1))
		return (NULL);
	p = malloc(count * size);
	if (!p)
		return (NULL);
	ft_bzero(p, count * size);
	return (p);
}
